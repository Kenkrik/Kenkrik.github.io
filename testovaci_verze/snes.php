			<p class="text">
				Super Nintendo Entertainment System (SNES), [b] běžně zkrácený na Super NES nebo Super Nintendo, [c] je 16bitová domácí videoherní konzole vyvinutá společností Nintendo, která byla vydána v roce 1990 v Japonsku a Jižní Koreji, [16] 1991 v Severní Americe, 1992 v Evropě a Austrálii (Oceánii) a 1993 v Jižní Americe. V Japonsku se systém nazývá Super Famicom (SFC). [D] V Jižní Koreji je známý jako Super Comboy [e] a byl distribuován společností Hyundai Electronics. [17] Systém byl vydán v Brazílii 30. srpna 1993 [16] [18] společností Playtronic. Ačkoli je každá verze v podstatě stejná, několik forem regionálního blokování brání vzájemné kompatibilitě různých kazet.
				<br>
				SNES je druhá programovatelná domácí konzole Nintendo po Nintendo Entertainment System (NES). Konzole představila pokročilé grafické a zvukové funkce ve srovnání s jinými systémy v té době. Systém byl navržen tak, aby vyhovoval pokračujícímu vývoji řady vylepšovacích čipů integrovaných do herních kazet, aby byl konkurenceschopný pro příští generaci.
				<br>
				SNES získal převážně pozitivní recenze a byl celosvětovým úspěchem a stal se nejprodávanější konzolou 16bitové éry i přesto, že byl spuštěn relativně pozdě a čelil intenzivní konkurenci konzoly Genesis společnosti Sega v Severní Americe a Evropě. Překrývající se 61,9 milionu prodaných jednotek NES, SNES zůstal populární až do 32bitové éry, s 49,1 miliony prodaných kusů po celém světě v době, kdy byla ukončena v roce 2003. Je stále populární mezi sběrateli a retro hráči, s novými hrami domácí hry a emulované nové vydání Nintendo, například na Virtual Console, Super NES Classic Edition a Nintendo Switch Online.	

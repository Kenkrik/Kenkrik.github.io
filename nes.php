		<h2 class="heading_normal">Klony a emulace</h2>
	
    
    
        <div class="blok">
		    <p class="text">
			    Konzole NES se stala velice oblíbenou pro různé napodobeniny. U nás jste již v 91. letech mohli snadno sehnat v kterékoliv z početných vietnamských tržnic. Kromě modelu, který byl podobný i vizuálně, se daly sehnat například – PolyStation – NES v podobě prvního Playstationu, ale i Mega Kid MK-1000, který má vlastní klávesnici, a dá se pod ním provozovat jednoduchá verze Basicu. Klonů bylo více typů – například jen v podobě ovladače, který se připojil k TV anebo přenosný PocketFami. V současnosti je známo přes 377 druhů všemožných napodobenin NESu/Famicomu.
			    <br>
			    Systém je velice oblíbený pro velké množství emulátorů a to i přestože paradoxně patří k těm náročnějším, kvůli nestandardnímu používání procesoru. Existuje přes 40 emulátorů pro PC, ale setkat se s nimi můžeme na PDA, mobilních telefonech, jiných konzolích jako třeba GP2x. Za emulaci lze považovat i provozování her přes tzv. virtuální konzoli Virtual Console™ na Nintendu Wii a 3DS.
		    </p>

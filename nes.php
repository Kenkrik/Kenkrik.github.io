		<h2 class="heading_normal" style="margin-top:0;">Nintendo Entertainment System</h2>

        <div class="blok">
			<img src="https://upload.wikimedia.org/wikipedia/commons/thumb/8/82/NES-Console-Set.jpg/800px-NES-Console-Set.jpg" alt="konzole" class="img_right">

			<p class="text">
				<b>Nintendo Entertainment System</b> (zkráceně <b>NES</b>) je se svými 60 miliony celosvětově prodanými kusy nejúspěšnější 8bitová herní konzole. Prodávat se začala v roce 1985 a to v Severní Americe, Evropě, Brazílii a Austrálii. Vývojově vycházela z herní konzole Nintendo Family Computer (zkráceně Famicom nebo FC) vydané pro japonský trh již v roce 1983. Díky tzv. krachu videoherního průmyslu přistupovalo Nintendo k americkému trhu specifickým způsobem a to dalo vzniknout nejen odlišnému designu konzole oproti japonské verzi, ale také odlišnému typu herních cartridgí (herní kazeta NESu je založena na konektoru se 8 piny, zatímco pro FC jich měla pouze 60) či zcela novému hernímu příslušenství. Herní konzole Nintendo NES patří do takzvané třetí generace herních konzolí a dodnes se mezi hráči těší velké oblibě. Další významné konzole třetí generace kromě NESu jsou Master System od Segy a Atari 7800. Opravdovým konkurentem pro ni byl jen Master System, který ovšem nebyl v Americe a ani doma v Japonsku úspěšný a NESu konkuroval jen na evropských trzích.
			</p>
		</div>


